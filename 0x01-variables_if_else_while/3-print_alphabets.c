#include <stdio.h>
/**
 * main - main block
 * Description: Print alphabet in lowercase and then in uppercase.
 * code by Tohluh122
 * Return: 0
 */
int main(void)
{
	char c = 'a';

	while (c <= 'z')
	{
		putchar(c);
		c++;
	}

	c = 'A';

	while (c <= 'Z')
	{
		putchar(c);
		c++;
	}

	putchar('\n');
	return (0);
}
